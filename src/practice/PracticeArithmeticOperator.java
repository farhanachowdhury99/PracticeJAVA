package practice;

public class PracticeArithmeticOperator {

    public static void main (String[] args) {
        int num1 = 25;
        int num2 = 35;
        int total = num1 + num2;
        int price = num1 - num2;
        int balance = num1 * num2;
        int value = num2 / num1;
        int remainder = num2 % num1;

        System.out.println("The total value is " + total);
        System.out.println("The total price is " + price);
        System.out.println("The total balance is " + balance);
        System.out.println("The total value is " + value);
        System.out.println("The total remainder is " + remainder);


    }
}
